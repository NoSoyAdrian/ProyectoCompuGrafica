// Moves/alters the camera positions based on user input
void DoMovement()
{

	// Camera controls
	if (keys[GLFW_KEY_W] || keys[GLFW_KEY_UP])
	{
		camera.ProcessKeyboard(FORWARD, deltaTime);

	}

	if (keys[GLFW_KEY_S] || keys[GLFW_KEY_DOWN])
	{
		camera.ProcessKeyboard(BACKWARD, deltaTime);


	}

	if (keys[GLFW_KEY_A] || keys[GLFW_KEY_LEFT])
	{
		camera.ProcessKeyboard(LEFT, deltaTime);


	}

	if (keys[GLFW_KEY_D] || keys[GLFW_KEY_RIGHT])
	{
		camera.ProcessKeyboard(RIGHT, deltaTime);


	}

	//Primera Animacion de saludo
	if (anim)
	{
		if (!anim2 && rotStan < 20.0f)
			rotStan += 0.5f;
		else if (anim2 && rotStan > 0.0f)
			rotStan -= 0.5f;
		if (rotStan >= 20.0f)
			anim2 = true;
		else if (rotStan <= 0.0f)
			anim2 = false;
	}

	//Segunda Animacion de cerrar puerta
	if (anim3) {
		if (!Open) {
			if (rotPuerta > -90.0f) {
				rotPuerta -= 0.5f;
			}
			else {
				anim3 = false;
			}
		}
		else {
			if (rotPuerta < 0.0f) {
				rotPuerta += 0.5f;
			}
			else {
				anim3 = false;
			}
		}
	}

	//Tercera animacion de cajon
	if (CajonAbierto) {

		if (CajonAbrir)
		{
			movCajon += 0.01;
			if (movCajon >= 0.4f)
			{
				CajonAbrir = false;
			}

		}
		if (CajonCerrar)
		{
			movCajon -= 0.01;
			if (movCajon <= 0.02f)
			{
				CajonCerrar = false;

			}
		}

	}


}

// Is called whenever a key is pressed/released via GLFW
void KeyCallback(GLFWwindow* window, int key, int scancode, int action, int mode)
{
	if (GLFW_KEY_ESCAPE == key && GLFW_PRESS == action)
	{
		glfwSetWindowShouldClose(window, GL_TRUE);
	}

	if (key >= 0 && key < 1024)
	{
		if (action == GLFW_PRESS)
		{
			keys[key] = true;
		}
		else if (action == GLFW_RELEASE)
		{
			keys[key] = false;
		}
	}
	//Boton para activar animacion 1
	if (keys[GLFW_KEY_Z])
	{
		anim = true;
	}
	//Boton para activar animacion 2
	if (keys[GLFW_KEY_X]) {
		Open = !Open;
		anim3 = true;

	}
	//Boton para activar animacion 3
	if (keys[GLFW_KEY_C])
	{
		CajonAbierto = true;
		if (movCajon >= 0.4f) {
			CajonCerrar = true;
			CajonAbrir = false;
		}
		else if (movCajon <= 0.02f) {
			CajonCerrar = false;
			CajonAbrir = true;

		}

	}
}

void MouseCallback(GLFWwindow* window, double xPos, double yPos)
{
	if (firstMouse)
	{
		lastX = xPos;
		lastY = yPos;
		firstMouse = false;
	}

	GLfloat xOffset = xPos - lastX;
	GLfloat yOffset = lastY - yPos;  // Reversed since y-coordinates go from bottom to left

	lastX = xPos;
	lastY = yPos;

	camera.ProcessMouseMovement(xOffset, yOffset);
}